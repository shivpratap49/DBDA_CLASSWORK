# List collection
# - loop
#   - for loop
#     - syntax
#       - for <temp variable> in <collection:
#            # code here
#   - for...else loop
#   - while loop
#   - while...else loop

def function1():
    # list of numbers
    numbers = [10, 20, 30, 40, 50]

    # iterate over list using for loop
    # by default the for loop starts from 0th location
    # and go till the last value of the collection
    for value in numbers:
        print(f"value = {value}")


function1()


