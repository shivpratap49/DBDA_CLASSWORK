# type conversion

# implicit type conversion

# int
num = 20

# float
value = 15.50

# here num (int) gets converted into float
# num = 20 => 20.0
# result will be in float
result = num + value
print(f"result = {result}")

# str
my_var = "test"

# since my_var can not be converted to int
# or num can not be converted to str automatically,
# here, the implicit type cast is not possible
# my_result = my_var + num

