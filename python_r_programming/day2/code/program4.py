# operators

# arithmetic operators
n1 = 21
n2 = 40
print(f"n1 + n2 = {n1 + n2}")
print(f"n1 - n2 = {n1 - n2}")

# when the operands are of numeric (int, float)
# you will get as mathematical multiplication
print(f"n1 * n2 = {n1 * n2}")

# true division
# - always results in float value
print(f"n1 / n2 = {n1 / n2}")
print(f"n1 % n2 = {n1 % n2}")

# floor division
# - always results in int value
print(f"n1 // n2 = {n1 // n2}")

# when one operand is string and another is int
# then, the string will be repeated 5 times
print(f"'*' * 5 = {'*' * 5}")
print(f"'-+-' * 5 = {'-+-' * 5}")
print(f"'-' * 80 = {'-' * 80}")

print(f"10 to the power of 2 = {10 ** 2}")
print(f"10 to the power of 3 = {10 ** 3}")

print('-' * 80)

# comparison operators
print(f"n1 > n2 = {n1 > n2}")
print(f"n1 >= n2 = {n1 >= n2}")
print(f"n1 < n2 = {n1 < n2}")
print(f"n1 <= n2 = {n1 <= n2}")
print(f"n1 == n2 = {n1 == n2}")
print(f"n1 != n2 = {n1 != n2}")

print('-' * 80)

# logical operators
print(f"T and T = {True and True}")
print(f"T and F = {True and False}")
print(f"F and T = {False and True}")
print(f"F and F = {False and False}")

print('-' * 80)

print(f"T or T = {True or True}")
print(f"T or F = {True or False}")
print(f"F or T = {False or True}")
print(f"F or F = {False or False}")

print('-' * 80)

print(f"not T = {not True}")
print(f"not F = {not False}")

